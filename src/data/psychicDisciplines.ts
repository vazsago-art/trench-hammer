/** Get all disciplines available to a faction */
/** Lookup map: discipline id → PsychicDiscipline */
const ALL_DISCIPLINES: Record<string, PsychicDiscipline> = {};

/** Extra disciplines (e.g. subfaction overrides) registered separately from FACTION_DISCIPLINES */
const SUBFACTION_OVERRIDE_DISCIPLINES: PsychicDiscipline[] = [
  discipline_contagion,
  discipline_excess,
  discipline_malefic,
];

function _registerDisciplines() {
  const allValues = [
    ...Object.values(FACTION_DISCIPLINES).flat(),
    ...SUBFACTION_OVERRIDE_DISCIPLINES,
  ];
  for (const d of allValues) {
    ALL_DISCIPLINES[d.id] = d;
  }
}
_registerDisciplines();

/**
 * Returns the psychic disciplines available for a faction.
 * When `subfactionDisciplineOverrideId` is supplied (a subfaction has its own
 * psychic discipline), that discipline replaces the faction's default list.
 * If the override ID is not found in the registry, the faction default is used.
 */
export function getDisciplinesForFaction(
  factionId: string,
  subfactionDisciplineOverrideId?: string,
): PsychicDiscipline[] {
  if (subfactionDisciplineOverrideId) {
    const override = ALL_DISCIPLINES[subfactionDisciplineOverrideId];
    if (override) return [override];
  }
  return FACTION_DISCIPLINES[factionId] ?? [];
}

/** Check if a faction has any psychic disciplines */
export function factionHasPsychicDisciplines(factionId: string): boolean {
  return (FACTION_DISCIPLINES[factionId]?.length ?? 0) > 0;
}

/**
 * Lookup a single PsychicPower from any discipline.
 * Returns undefined if the discipline or power is not found.
 */
export function lookupPsychicPower(disciplineId: string, powerId: string) {
  const disc = ALL_DISCIPLINES[disciplineId];
  if (!disc) return undefined;
  return disc.powers.find(p => p.id === powerId);
}
